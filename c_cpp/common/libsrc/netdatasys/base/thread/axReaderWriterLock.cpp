//********************************************************************
// default constructor:
//********************************************************************
axReaderWriterLock::axReaderWriterLock()
{
  pthread_mutex_init(&m_readWriteLock, NULL);
}


//********************************************************************
// destructor:
//********************************************************************
axReaderWriterLock::~axReaderWriterLock()
{
  pthread_mutex_destroy(&m_readWriteLock);
}


//********************************************************************
// data constructor:
//********************************************************************
// axReaderWriterLock::axReaderWriterLock()
