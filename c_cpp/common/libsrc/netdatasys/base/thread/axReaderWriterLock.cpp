//********************************************************************
// method:
//********************************************************************
void
axReaderWriterLock::readPreAccess(void)
{
  pthread_mutex_lock(&m_readWriteLock);
}

//********************************************************************
// method:
//********************************************************************
void
axReaderWriterLock::readPostAccess(void)
{
  pthread_mutex_unlock(&m_readWriteLock);
}

//********************************************************************
// method:
//********************************************************************
void
axReaderWriterLock::writePreAccess(void)
{
  pthread_mutex_lock(&m_readWriteLock);
}

//********************************************************************
// method:
//********************************************************************
void
axReaderWriterLock::writePostAccess(void)
{
  pthread_mutex_unlock(&m_readWriteLock);
}

