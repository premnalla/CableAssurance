// data constructor:
//********************************************************************
axWriteLockOperator::axWriteLockOperator(axAbstractLock * lock) :
  m_lock(lock)
{
#if 1
  m_lock->writePreAccess();
  // printf("WritePreAccess...\n");
#endif
}


