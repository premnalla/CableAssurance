//********************************************************************
// default constructor:
//********************************************************************
axIteratorHolder::axIteratorHolder() :
  m_iter(NULL)
{
}


//********************************************************************
// destructor:
//********************************************************************
axIteratorHolder::~axIteratorHolder()
{
  if (m_iter) {
    delete m_iter;
  }
}


//********************************************************************
// data constructor:
//********************************************************************
axIteratorHolder::axIteratorHolder(axAbstractIterator * iter) :
  m_iter(iter)
{
}


//********************************************************************
// method:
//********************************************************************
axAbstractIterator *
axIteratorHolder::getIterator(void)
{
  return (m_iter);
}


