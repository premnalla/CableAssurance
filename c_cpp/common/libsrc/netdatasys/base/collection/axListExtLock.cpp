//********************************************************************
// default constructor:
//********************************************************************
axListExtLock::axListExtLock() :
  m_lock(NULL)
{
}

//********************************************************************
// destructor:
//********************************************************************
axListExtLock::~axListExtLock()
{
  if (m_lock) {
    delete m_lock;
  }
}

//********************************************************************
// data constructor:
//********************************************************************
axListExtLock::axListExtLock(axAbstractLock * lock) :
  m_lock(lock)
{
}


//********************************************************************
// method: isEmpty
//********************************************************************
axAbstractLock *
axListExtLock::getLock(void)
{
  return (m_lock);
}

