//********************************************************************
// method: lock
//********************************************************************
void
axLongHashMap_mts::lock(void)
{
  pthread_mutex_lock(&m_collectionLock);
}


//********************************************************************
// method: unlock
//********************************************************************
void
axLongHashMap_mts::unlock(void)
{
  pthread_mutex_unlock(&m_collectionLock);
}


