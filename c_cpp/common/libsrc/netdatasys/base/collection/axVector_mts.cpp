//********************************************************************
// default constructor:
//********************************************************************
axVector_mts::axVector_mts()
{
  pthread_mutex_init(&m_collectionLock, NULL);
}

//********************************************************************
// destructor:
//********************************************************************
axVector_mts::~axVector_mts()
{
  pthread_mutex_destroy(&m_collectionLock);
}

//********************************************************************
// data constructor:
//********************************************************************
// axVector_mts::axVector_mts()
