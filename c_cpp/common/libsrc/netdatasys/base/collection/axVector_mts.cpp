//********************************************************************
// method: lock
//********************************************************************
void
axVector_mts::lock(void)
{
  pthread_mutex_lock(&m_collectionLock);
}


//********************************************************************
// method: unlock
//********************************************************************
void
axVector_mts::unlock(void)
{
  pthread_mutex_unlock(&m_collectionLock);
}


