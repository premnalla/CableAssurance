axStrNVPair::axStrNVPair() :
  axString(""), 
  m_value("")
{
}

//********************************************************************
// destructor:
//********************************************************************
axStrNVPair::~axStrNVPair()
{
}


//********************************************************************
// data constructor:
//********************************************************************
axStrNVPair::axStrNVPair(const char * name, const char * value) :
  axString(name),
  m_value(value)
{
}


//********************************************************************
// method : getName
//********************************************************************
auto_ptr<string>
axStrNVPair::getName(void)
{
  return (hashString());
}


//********************************************************************
// method : getName
//********************************************************************
auto_ptr<string>
axStrNVPair::getValue(void)
{
  auto_ptr<string> str(new string(m_value));
  return (str);
}


