axUnsignedInteger::~axUnsignedInteger()
{
}


//********************************************************************
// data constructor:
//********************************************************************
axUnsignedInteger::axUnsignedInteger(AX_UINT32 val) : 
  m_data(val)
{
}


//********************************************************************
// method: toString
//********************************************************************
auto_ptr<string>
axUnsignedInteger::toString(void) 
{
  char tmpS[64];
  sprintf(tmpS, "%d", m_data);
  auto_ptr<string> ret(new string(tmpS));

  return (ret);
}


//********************************************************************
// method : hashCode
//********************************************************************
AX_INT32
axUnsignedInteger::hashInt32(void)
{
  return ((AX_INT32) m_data);
}


//********************************************************************
// method : hashInt
//********************************************************************
AX_UINT32
axUnsignedInteger::hashUInt32(void)
{
  return ((AX_UINT32)m_data);
}


