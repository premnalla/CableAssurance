//********************************************************************
// default constructor:
//********************************************************************
axAbstractWrappedStream::axAbstractWrappedStream() :
  m_fd(0), m_fClosed(false), m_sockFailHandler(NULL)
{
}


//********************************************************************
// destructor:
//********************************************************************
axAbstractWrappedStream::~axAbstractWrappedStream()
{
}


//********************************************************************
// data constructor:
//********************************************************************
axAbstractWrappedStream::axAbstractWrappedStream(AX_INT32 fd,
                                        axSocketFailInterface * sfh) :
  m_fd(fd), m_fClosed(false), m_sockFailHandler(sfh)
{
}


//********************************************************************
// method:
//********************************************************************
AX_INT32
axAbstractWrappedStream::getFd(void)
{
  return (m_fd);
}


//********************************************************************
// method:
//********************************************************************
void
axAbstractWrappedStream::setClosed(bool b)
{
  m_fClosed = b;
}


//********************************************************************
// method:
//********************************************************************
bool
axAbstractWrappedStream::isClosed(void)
{
  return (m_fClosed);
}


//********************************************************************
// method:
//********************************************************************
axSocketFailInterface *
axAbstractWrappedStream::getSocketFailHandler(void)
{
  return (m_sockFailHandler);
}


