axTFSystemConfig * axTFSystemConfig::m_instance = NULL;

//********************************************************************
// forward declerations
//********************************************************************


//********************************************************************
// default constructor:
//********************************************************************
axTFSystemConfig::axTFSystemConfig() :
  axLockableObject(new axMultipleReaderLock()),
  m_initialized(false),
  m_serverPortNumber(0)
{
}


//********************************************************************
// destructor:
//********************************************************************
axTFSystemConfig::~axTFSystemConfig()
{
}


//********************************************************************
// data constructor:
