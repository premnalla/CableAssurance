//********************************************************************
// method:
//********************************************************************
axTFSystemConfig *
axTFSystemConfig::getInstance(void)
{
  if (!m_instance) {
    m_instance = new axTFSystemConfig();
  }
  return (m_instance);
}


//********************************************************************
// method:
//********************************************************************
void
axTFSystemConfig::setServerPortNumber(const AX_INT8 * val)
{
  m_serverPortNumber = atoi(val);
}


//********************************************************************
// method:
//********************************************************************
AX_UINT32
axTFSystemConfig::getServerPortNumber(void)
{
  return (m_serverPortNumber);
}


