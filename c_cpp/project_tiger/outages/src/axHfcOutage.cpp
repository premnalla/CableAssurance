//********************************************************************
// default constructor:
//********************************************************************
axHfcOutage::axHfcOutage() :
  m_cmtsResId(0)
{
  m_hfcName[0] = '\0';
}


//********************************************************************
// destructor:
//********************************************************************
axHfcOutage::~axHfcOutage()
{
}


//********************************************************************
// data constructor:
//********************************************************************
axHfcOutage::axHfcOutage(INTDS_RESID_t cmtsResId, AX_INT8 * hfcName) :
  m_cmtsResId(cmtsResId)
{
  strcpy(m_hfcName, hfcName);
}


//********************************************************************
// method:
//********************************************************************
INTDS_RESID_t
axHfcOutage::getCmtsResId(void)
{
  return (m_cmtsResId);
}


//********************************************************************
// method:
//********************************************************************
AX_INT8 *
axHfcOutage::getHfcName(void)
{
  return (m_hfcName);
}


//********************************************************************
// method:
//********************************************************************
int
axHfcOutage::keyCompare(axObject * o)
{
  int ret;

  axHfcOutage * b = dynamic_cast<axHfcOutage *> (o);

  ret = getCmtsResId() - b->getCmtsResId();

  if (ret) {
    goto EXIT_LABEL;
  }

  ret = strcmp(getHfcName(), b->getHfcName());

  if (ret) {
    goto EXIT_LABEL;
  }

  ret = getAlarmSubType() - b->getAlarmSubType();

EXIT_LABEL:

  return (ret);
}


//********************************************************************
// method:
//********************************************************************
AX_UINT8
axHfcOutage::getAlarmType(void)
{
  return (AX_OUTAGE_TYPE_HFC);
}


