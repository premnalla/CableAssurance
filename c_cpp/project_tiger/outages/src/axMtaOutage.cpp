//********************************************************************
// default constructor:
//********************************************************************
axMtaOutage::axMtaOutage() :
  m_cmtsResId(0)
{
  m_mtaMacAddress[0] = '\0';
}


//********************************************************************
// destructor:
//********************************************************************
axMtaOutage::~axMtaOutage()
{
}


//********************************************************************
// data constructor:
//********************************************************************
axMtaOutage::axMtaOutage(INTDS_RESID_t cmtsResId, AX_INT8 * mtaMac) :
  m_cmtsResId(cmtsResId)
{
  strcpy(m_mtaMacAddress, mtaMac);
}


//********************************************************************
// method:
//********************************************************************
INTDS_RESID_t
axMtaOutage::getCmtsResId(void)
{
  return (m_cmtsResId);
}


//********************************************************************
// method:
//********************************************************************
AX_INT8 *
axMtaOutage::getMtaMacAddress(void)
{
  return (m_mtaMacAddress);
}


//********************************************************************
// method:
//********************************************************************
int
axMtaOutage::keyCompare(axObject * o)
{
  int ret;

  axMtaOutage * b = dynamic_cast<axMtaOutage *> (o);

  ret = getCmtsResId() - b->getCmtsResId();

  if (ret) {
    goto EXIT_LABEL;
  }

  ret = strcmp(getMtaMacAddress(), b->getMtaMacAddress());

EXIT_LABEL:

  return (ret);
}


//********************************************************************
// method:
//********************************************************************
AX_UINT8
axMtaOutage::getAlarmType(void)
{
  return (AX_OUTAGE_TYPE_MTA);
}


