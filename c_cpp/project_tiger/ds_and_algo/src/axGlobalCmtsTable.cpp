//********************************************************************
// default constructor:
//********************************************************************
axGlobalCmtsTable::axGlobalCmtsTable()
{
  m_data.table = new axAvlTree();
  m_data.lock = new axMultipleReaderLock();
}


//********************************************************************
// destructor:
//********************************************************************
axGlobalCmtsTable::~axGlobalCmtsTable()
{
  delete m_data.table;
  delete m_data.lock;

  m_data.table = NULL;
  m_data.lock = NULL;
}


