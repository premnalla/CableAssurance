//********************************************************************
// default constructor:
//********************************************************************
axSnmpSessionHolder::axSnmpSessionHolder() :
  m_openSession(NULL)
{
}


//********************************************************************
// destructor:
//********************************************************************
axSnmpSessionHolder::~axSnmpSessionHolder()
{
  if (m_openSession) {
    delete m_openSession;
  }
}


//********************************************************************
// data constructor:
//********************************************************************
axSnmpSessionHolder::axSnmpSessionHolder(axSnmpSession * s) :
  m_openSession(s)
{
}


//********************************************************************
// method:
//********************************************************************
axSnmpSession *
axSnmpSessionHolder::getSession(void)
{
  return (m_openSession);
}


