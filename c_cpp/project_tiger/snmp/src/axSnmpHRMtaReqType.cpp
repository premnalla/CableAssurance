axSnmpHRMtaReqType * axSnmpHRMtaReqType::m_instance = NULL;

//********************************************************************
// forward declerations
//********************************************************************


//********************************************************************
// default constructor:
//********************************************************************
axSnmpHRMtaReqType::axSnmpHRMtaReqType()
{
  int i = 0;

  if (snmp_parse_oid(SNMP_MTA_PROV_STATUS_OID, 
       m_mtaOids.oids[i].nativeOid, &m_mtaOids.oids[i].nativeOidLen)) {
    i++;
  } else {
    snmp_perror(SNMP_MTA_PROV_STATUS_OID);
  }

  m_mtaOids.numOids = i;

  setMaxRepetitions(1);
}


//********************************************************************
// destructor:
//********************************************************************
axSnmpHRMtaReqType::~axSnmpHRMtaReqType()
{
}


