//********************************************************************
// method:
//********************************************************************
axSnmpHRMtaReqType *
axSnmpHRMtaReqType::getInstance(void)
{
  if (!m_instance) {
    m_instance = new axSnmpHRMtaReqType();
  }

  return (m_instance);
}


//********************************************************************
// method:
//********************************************************************
axSnmpOidsBase_s *
axSnmpHRMtaReqType::getOids(void)
{
  return (&m_mtaOids);
}


