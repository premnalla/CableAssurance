//********************************************************************
// default constructor:
//********************************************************************
axSnmpV3SessionHelper::axSnmpV3SessionHelper()
{
}


//********************************************************************
// destructor:
//********************************************************************
axSnmpV3SessionHelper::~axSnmpV3SessionHelper()
{
  axSnmpSessionFactory * cf = getSessionFactory();
  axSnmpSession * s = getSession();
  if (s->isOpen()) {
    s->close();
  }
  cf->releaseV3Session(s);
}


//********************************************************************
// data constructor:
//********************************************************************
axSnmpV3SessionHelper::axSnmpV3SessionHelper(axSnmpSessionFactory * cf)
{
  axSnmpSession * c = cf->getV3Session();

  while (c == NULL) {
    sleep(1);
    c = cf->getV3Session();
  }

  setSession(c);
  setSessionFactory(cf);
}


