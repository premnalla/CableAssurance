axDbQueryHelper::axDbQueryHelper() :
  m_queryObj(NULL)
{
}


//********************************************************************
// destructor:
//********************************************************************
axDbQueryHelper::~axDbQueryHelper()
{
  if (m_queryObj) {
    delete m_queryObj;
  }
}


