// data constructor:
//********************************************************************
axDbQueryHelper::axDbQueryHelper(axDbGenericQuery * q) :
  m_queryObj(q)
{
}


//********************************************************************
// method:
//********************************************************************
axDbGenericQuery *
axDbQueryHelper::getQuery(void)
{
  return (m_queryObj);
}


