AX_INT32 
axPollerUtils::GetNumRunnables(AX_INT32 numElemes, AX_INT32 maxRunnables)
{
  AX_INT32 ret = 0;

  ret = numElemes / maxRunnables;
  if (numElemes % maxRunnables) {
    ++ret;
  }

  return (ret);
}

//********************************************************************
// method:
//********************************************************************
AX_INT32
axPollerUtils::GetNumElementsPerRunnable(AX_INT32 numElements, 
                                                AX_INT32 numRunnables)
{
  AX_INT32 ret = 0;

  ret = numElements / numRunnables;

  /* Need this!!! to get around remainders in the division */
  ret += numRunnables;

  return (ret);
}


